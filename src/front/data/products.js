// Funciones helper
export const getProductById = (id) => {
  const productId = parseInt(id);
  return productsData.find((product) => product.id === productId);
};

export const getProductsByBrand = (brand) => {
  if (brand === "all") return productsData;
  return productsData.filter((product) => product.brand === brand);
};

export const getProductsByCategory = (category) => {
  if (category === "all") return productsData;
  return productsData.filter((product) => product.category === category);
};

export const getUniqueBrands = () => {
  return ["all", ...new Set(productsData.map((product) => product.brand))];
};

export const getUniqueCategories = () => {
  return ["all", ...new Set(productsData.map((product) => product.category))];
};

export const searchProducts = (searchTerm) => {
  if (!searchTerm) return productsData;

  const term = searchTerm.toLowerCase();
  return productsData.filter(
    (product) =>
      product.name.toLowerCase().includes(term) ||
      product.brand.toLowerCase().includes(term) ||
      product.description.toLowerCase().includes(term) ||
      product.model.toLowerCase().includes(term)
  );
};

// Estadísticas
export const getCatalogStats = () => {
  const totalProducts = productsData.length;
  const totalBrands = getUniqueBrands().length - 1; // Excluye "all"
  const prices = productsData.map((p) => p.price);

  return {
    totalProducts,
    totalBrands,
    priceRange: {
      min: Math.min(...prices),
      max: Math.max(...prices),
    },
    totalStock: productsData.reduce((sum, product) => sum + product.stock, 0),
  };
};
